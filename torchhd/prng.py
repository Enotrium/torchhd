def export_lfsr_verilog(
    lfsr: LFSR,
    filepath: str,
    *,
    module_name: str = "lfsr",
    clk_name: str = "clk",
    rst_name: str = "rst_n",
    en_name: str = "en",
    out_name: str = "out",
) -> None:
    r"""Export a synthesizable Verilog LFSR module matching the Python instance.

    The generated module is a standard Galois LFSR that, when reset and clocked
    identically, produces the same output bitstream as the Python :class:`LFSR`.

    Args:
        lfsr: The Python LFSR instance to export.
        filepath: Output ``.v`` file path.
        module_name: Verilog module name.
        clk_name: Clock signal name.
        rst_name: Reset signal name (active low).
        en_name: Enable signal name.
        out_name: Output bit signal name.
    """
    width = lfsr.width
    poly = lfsr.polynomial
    seed = lfsr.state  # current state (or original seed would be saved)

    # Format the polynomial as a bit string for readability
    poly_bits = f"{poly:0{width}b}"

    lines = []
    lines.append("// Auto-generated Galois LFSR")
    lines.append(f"// Width: {width}, Polynomial: 0x{poly:0{width//4}x}")
    lines.append(f"// Seed: 0x{seed:0{width//4}x}")
    lines.append("")
    lines.append(f"module {module_name} (")
    lines.append(f"  input  wire        {clk_name},")
    lines.append(f"  input  wire        {rst_name},")
    lines.append(f"  input  wire        {en_name},")
    lines.append(f"  output wire        {out_name}")
    lines.append(");")
    lines.append("")
    lines.append(f"  reg [{width-1}:0] lfsr_state;")
    lines.append("")
    lines.append(f"  assign {out_name} = lfsr_state[0];")
    lines.append("")
    lines.append(f"  always @(posedge {clk_name} or negedge {rst_name}) begin")
    lines.append(f"    if (!{rst_name}) begin")
    lines.append(f"      lfsr_state <= {width}'h{seed:0{(width+3)//4}x};")
    lines.append(f"    end else if ({en_name}) begin")
    lines.append(f"      if (lfsr_state[0])")
    lines.append(f"        lfsr_state <= (lfsr_state >> 1) ^ {width}'h{poly:0{(width+3)//4}x};")
    lines.append(f"      else")
    lines.append(f"        lfsr_state <= lfsr_state >> 1;")
    lines.append(f"    end")
    lines.append(f"  end")
    lines.append("")
    lines.append(f"endmodule")

    with open(filepath, "w") as f:
        f.write("\n".join(lines) + "\n")